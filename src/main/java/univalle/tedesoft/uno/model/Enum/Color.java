package univalle.tedesoft.uno.model.Enum;

/**
 * Enum que representa los colores posibles de una carta en el juego UNO.
 * Incluye los cuatro colores clasicos y el tipo especial WILD usado por las cartas comodin.
 * @author David Esteban Valencia
 * @author Santiago David Guerrero
 * @author Juan Pablo Escamilla
 */
public enum Color {
    // Enum para los colores de carta
    /** Color Rojo. */
    RED,
    /** Color Amarillo. */
    YELLOW,
    /** Color Verde. */
    GREEN,
    /** Color Azul. */
    BLUE,
    /** Color especial para comodines, indica que el color puede ser elegido. */
    WILD
}